package ar.com.cac.modelos;

public class futbol {

	private int idCancha;
	private String tipoCancha;
	private String cantidadPorEquipo;
	private Double precio;
	private String type;
	private String imagenes;
	public int getIdCancha() {
		return idCancha;
	}
	public void setIdCancha(int idCancha) {
		this.idCancha = idCancha;
	}
	public String getTipoCancha() {
		return tipoCancha;
	}
	public void setTipoCancha(String tipoCancha) {
		this.tipoCancha = tipoCancha;
	}
	public String getCantidadPorEquipo() {
		return cantidadPorEquipo;
	}
	public void setCantidadPorEquipo(String cantidadPorEquipo) {
		this.cantidadPorEquipo = cantidadPorEquipo;
	}
	public Double getPrecio() {
		return precio;
	}
	public void setPrecio(Double precio) {
		this.precio = precio;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getImagenes() {
		return imagenes;
	}
	public void setImagenes(String imagenes) {
		this.imagenes = imagenes;
	}
	
	
}
